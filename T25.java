package test;

public class T25 {
	public static void main(String[] args){
		System.out.println(Math.abs(-26.7)); /* will print how far away the value is from zero*/
		System.out.println(Math.ceil(7.4)); /* will round up even if its not x.5 - floor rounds down*/
		System.out.println(Math.max(8.6, 5.2)); /* out of the two numbers it will print the maximum number - min does the opposite*/
		System.out.println(Math.pow(8, 2)); /* will use the formula a^b*/
		System.out.println(Math.sqrt(144)); /* give sqrt */
	}
}
